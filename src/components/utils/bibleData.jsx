export function isValidChapter(book, chapter) {
  const maxChapters = BIBLE_BOOKS[book];
  if (!maxChapters) return false;
  return chapter >= 1 && chapter <= maxChapters;
}

export function generateAccessKey() {
  const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  let key = 'BRN-';
  for (let i = 0; i < 4; i++) {
    for (let j = 0; j < 4; j++) {
      key += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    if (i < 3) key += '-';
  }
  return key;
}

export function generateGrandmasterKey() {
  return 'GRANDMASTER-BEREAN-' + Math.random().toString(36).substring(2, 10).toUpperCase();
}